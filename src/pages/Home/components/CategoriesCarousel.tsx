import React, { useEffect, useState } from 'react';
import { useNavigate } from 'react-router-dom';
import { motion } from 'framer-motion';
import { supabase } from '@/integrations/supabase/client';
import {
  Carousel,
  CarouselContent,
  CarouselItem,
  CarouselNext,
  CarouselPrevious,
  type CarouselApi,
} from "@/components/ui/carousel";

interface Category {
  id: string;
  name: string;
  image_url: string;
  description: string;
}

const CategoriesCarousel = () => {
  const navigate = useNavigate();
  const [categories, setCategories] = useState<Category[]>([]);
  const [loading, setLoading] = useState(true);
  const [api, setApi] = useState<CarouselApi>();

  // Auto-scroll logic
  useEffect(() => {
    if (!api) return;

    const intervalId = setInterval(() => {
      api.scrollNext();
    }, 4000);

    return () => clearInterval(intervalId);
  }, [api]);

  // Fallback images matching the luxury sweets aesthetic
  const fallbacks = [
    'https://images.unsplash.com/photo-1599354607481-99c75a02797e?auto=format&fit=crop&q=80', // Sweets
    'https://images.unsplash.com/photo-1605196377314-e575aa975b9f?auto=format&fit=crop&q=80', // Ladoo
    'https://images.unsplash.com/photo-1517244683847-7456b63c5d69?auto=format&fit=crop&q=80', // Cake/Dessert
    'https://images.unsplash.com/photo-1587314168485-3236d6710814?auto=format&fit=crop&q=80', // Indian Sweets
    'https://images.unsplash.com/photo-1569383746724-6f1b88e9c906?auto=format&fit=crop&q=80', // Gulab Jamun
  ];

  useEffect(() => {
    fetchCategories();
  }, []);

  const fetchCategories = async () => {
    try {
      const { data, error } = await supabase
        .from('categories')
        .select('*')
        .eq('is_active', true)
        .order('name');

      if (error) throw error;
      setCategories(data || []);
    } catch (error) {
      console.error('Error fetching categories:', error);
    } finally {
      setLoading(false);
    }
  };

  const handleCategoryClick = (category: Category) => {
    navigate(`/menu?category=${category.name}`);
  };

  if (loading) {
    return (
      <section className="py-20 bg-background">
        <div className="container mx-auto px-4">
          <div className="flex justify-center items-center h-40">
            <div className="w-8 h-8 border-2 border-primary border-t-transparent rounded-full animate-spin"></div>
          </div>
        </div>
      </section>
    );
  }

  if (categories.length === 0) return null;

  return (
    <section className="py-12 md:py-16 bg-background">
      <div className="w-full max-w-[1800px] mx-auto px-4 md:px-8 lg:px-12">

        {/* Section Title - Minimal */}
        <div className="flex flex-col items-center justify-center mb-10 space-y-3">
          <span className="text-[10px] uppercase tracking-[0.25em] text-primary font-medium font-orange-avenue">CURATED FOR YOU</span>
          <h2 className="text-xl md:text-2xl lg:text-3xl text-foreground uppercase font-orange-avenue font-normal tracking-wide">
            THE MENU
          </h2>
          <div className="w-12 h-0.5 bg-primary/30 mt-2"></div>
        </div>

        {/* Categories Carousel */}
        <div className="relative px-4 md:px-12">
          <Carousel
            setApi={setApi}
            opts={{
              align: "start",
              loop: true,
            }}
            className="w-full"
          >
            <CarouselContent className="-ml-4">
              {categories.map((category, index) => (
                <CarouselItem key={category.id} className="pl-4 basis-[70%] sm:basis-1/2 md:basis-1/3 lg:basis-1/4">
                  <motion.div
                    initial={{ opacity: 0, y: 20 }}
                    whileInView={{ opacity: 1, y: 0 }}
                    viewport={{ once: true }}
                    transition={{ delay: index * 0.1, duration: 0.6 }}
                  >
                    <div
                      className="group relative h-[350px] md:h-[450px] overflow-hidden cursor-pointer rounded-none"
                      onClick={() => handleCategoryClick(category)}
                    >
                      {/* Image */}
                      <div className="absolute inset-0 bg-gray-200">
                        <img
                          src={category.image_url || fallbacks[index % fallbacks.length]}
                          alt={category.name}
                          className="w-full h-full object-cover transition-transform duration-[1.5s] ease-in-out group-hover:scale-110"
                        />
                      </div>

                      {/* Overlay - Gradient always present for readability */}
                      <div className="absolute inset-0 bg-gradient-to-t from-black/80 via-transparent to-transparent opacity-60 group-hover:opacity-90 transition-opacity duration-500" />

                      {/* Category Name */}
                      <div className="absolute bottom-6 left-0 right-0 text-center px-4">
                        <h3 className="font-orange-avenue text-white text-lg md:text-xl tracking-widest font-normal uppercase transition-all duration-500 group-hover:-translate-y-1">
                          {category.name}
                        </h3>
                        <span className="inline-block mt-2 text-[10px] text-primary uppercase tracking-[0.2em] opacity-0 transform translate-y-2 transition-all duration-500 group-hover:opacity-100 group-hover:translate-y-0">
                          View Items
                        </span>
                      </div>
                    </div>
                  </motion.div>
                </CarouselItem>
              ))}
            </CarouselContent>
            <CarouselPrevious className="hidden md:flex -left-12 border-border bg-transparent hover:bg-primary text-foreground hover:text-white transition-colors" />
            <CarouselNext className="hidden md:flex -right-12 border-border bg-transparent hover:bg-primary text-foreground hover:text-white transition-colors" />
          </Carousel>
        </div>

      </div>
    </section>
  );
};

export default CategoriesCarousel;